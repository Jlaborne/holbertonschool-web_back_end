#!/usr/bin/env python3
"""
This module provides a function to add two floating-point numbers.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats and returns their sum.

    Args:
        a (float): First float.
        b (float): Second float.

    Returns:
        float: The sum of the two floats.
    """
    return a + b
